                            A Associação Beneficente “Caminho de Damasco”, também designada pela sigla A.B.C.D., constituída em 01 de Setembro de 1942, é uma entidade civil, sem fins lucrativos e sem fins econômicos, que terá duração por tempo indeterminado, com sede própria sita à Rua Benedito Pereira, 1874 – Bairro Estação, no município de Votuporanga, Estado de São Paulo.
